export function PixTypeIcon({
  type,
  size = 'md',
  disabled = false,
  showTooltip = true,
  className = '',
}: PixTypeIconProps) {
  const sizeConfig = {
    sm: { container: 'w-8 h-8', iconSize: 20 },
    md: { container: 'w-10 h-10', iconSize: 24 },
    lg: { container: 'w-12 h-12', iconSize: 28 },
  }

  const currentSize = sizeConfig[size]

  const tooltips: Record<PixType, string> = {
    PIX_IN: 'Pix Recebido',
    PIX_OUT: 'Pix Enviado',
    BLOQUEIO: 'Bloqueio Judicial',
    TAX: 'Taxa Administrativa',
  }

  const PixInIcon = () => (
    <svg
      width={currentSize.iconSize}
      height={currentSize.iconSize}
      viewBox="0 0 612 612"
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      className={`transition-transform duration-300 ${disabled ? 'opacity-50' : 'group-hover/icon:scale-110'}`}
    >
      <path clipRule="evenodd" d={pixInPaths.arc} fill="var(--chart-4)" fillRule="evenodd" />
      <path clipRule="evenodd" d={pixInPaths.cifrao} fill="var(--chart-4)" fillRule="evenodd" />
      <path clipRule="evenodd" d={pixInPaths.seta} fill="var(--chart-4)" fillRule="evenodd" />
    </svg>
  )

  const PixOutIcon = () => (
    <svg
      width={currentSize.iconSize}
      height={currentSize.iconSize}
      viewBox="0 0 612 612"
      fill="none"
      xmlns="http://www.w3.org/2000/svg"
      className={`transition-transform duration-300 ${disabled ? 'opacity-50' : 'group-hover/icon:scale-110'}`}
    >
      <path clipRule="evenodd" d={pixOutPaths.arc} fill="var(--chart-4)" fillRule="evenodd" />
      <path clipRule="evenodd" d={pixOutPaths.cifrao} fill="var(--chart-4)" fillRule="evenodd" />
      <path clipRule="evenodd" d={pixOutPaths.seta} fill="var(--chart-4)" fillRule="evenodd" />
    </svg>
  )

  if (type === 'PIX_IN' || type === 'PIX_OUT') {
    return (
      <div className={`group/icon relative inline-flex items-center justify-center ${className}`}>
        {type === 'PIX_IN' ? <PixInIcon /> : <PixOutIcon />}

        {showTooltip && (
          <div className="absolute left-1/2 -translate-x-1/2 bottom-full mb-2 px-3 py-1.5 bg-[color:var(--sidebar)] text-[color:var(--sidebar-foreground)] text-xs rounded shadow-lg opacity-0 group-hover/icon:opacity-100 transition-opacity pointer-events-none whitespace-nowrap z-10 font-['Montserrat',sans-serif]">
            {tooltips[type]}
            <div className="absolute left-1/2 -translate-x-1/2 top-full w-0 h-0 border-l-4 border-r-4 border-t-4 border-l-transparent border-r-transparent border-t-[color:var(--sidebar)]" />
          </div>
        )}
      </div>
    )
  }

  return null
}
